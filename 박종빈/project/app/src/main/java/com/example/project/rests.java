package com.example.project;

public class rests {
    String name; // 휴게소 이름
    Double x;  // 휴게소의 x좌표
    Double y;  // 휴게소의 y좌표

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Double getX() {
        return x;
    }

    public void setX(Double x) {
        this.x = x;
    }

    public Double getY() {
        return y;
    }

    public void setY(Double y) {
        this.y = y;
    }
}
